if __name__ == "__main__":
    print("Testing ReplayBuffer...")

    # Create buffer
    buffer = ReplayBuffer(capacity=4, group_size=2, filter_informative=True)
    print(f"Empty buffer: {buffer}")

    # Create sample rollout data
    def make_rollout_batch(question_ids: List[int], rewards_per_q: List[List[float]]):
        """Helper to create test rollout data."""
        n_questions = len(question_ids)
        group_size = len(rewards_per_q[0])
        n_rollouts = n_questions * group_size

        # Flatten rewards
        flat_rewards = []
        for r_list in rewards_per_q:
            flat_rewards.extend(r_list)

        # Expand question IDs for each rollout
        flat_indices = []
        flat_questions = []
        flat_solutions = []
        for i, qid in enumerate(question_ids):
            flat_indices.extend([qid] * group_size)
            flat_questions.extend([f"Question {qid}"] * group_size)
            flat_solutions.extend([f"Solution {qid}"] * group_size)

        return DataProto(
            batch={
                "input_ids": torch.randint(0, 100, (n_rollouts, 10)),
                "old_log_probs": torch.randn(n_rollouts, 10),
                "rewards": torch.tensor(flat_rewards),
                "advantages": torch.tensor(flat_rewards) - torch.tensor(flat_rewards).mean(),
            },
            non_tensor_batch={
                "index": flat_indices,
                "question": flat_questions,
                "solution": flat_solutions,
            }
        )

    # Test 1: Add informative samples
    print("\n--- Test 1: Adding informative samples ---")
    batch1 = make_rollout_batch(
        question_ids=[0, 1],
        rewards_per_q=[[1.0, 0.0], [1.0, 1.0]]  # Q0: informative, Q1: not informative
    )
    added = buffer.add(batch1)
    print(f"Added {added} questions (expected 1, Q1 filtered out)")
    print(f"Buffer: {buffer}")

    # Test 2: Add more and test deduplication
    print("\n--- Test 2: Deduplication ---")
    batch2 = make_rollout_batch(
        question_ids=[0, 2],  # Q0 is duplicate, Q2 is new
        rewards_per_q=[[0.0, 1.0], [1.0, 0.0]]  # Both informative
    )
    buffer.add(batch2)
    print(f"After adding Q0 (dup) and Q2: {buffer}")
    # Q0 should be deduplicated (keep most recent)

    # Test 3: Sampling
    print("\n--- Test 3: Sampling ---")
    sample = buffer.sample(num_samples=1)
    if sample:
        print(f"Sampled 1 question: indices={sample.non_tensor_batch['index'][:2]}")

    # Test 4: FIFO truncation
    print("\n--- Test 4: FIFO truncation ---")
    for i in range(3, 10):
        batch = make_rollout_batch(
            question_ids=[i],
            rewards_per_q=[[1.0, 0.0]]
        )
        buffer.add(batch)
    print(f"After adding many: {buffer}")
    print(f"Should be at capacity ({buffer.capacity})")

    # Test 5: combine_fresh_and_replay
    print("\n--- Test 5: Combine fresh and replay ---")
    fresh = make_rollout_batch(
        question_ids=[100, 101],
        rewards_per_q=[[1.0, 0.0], [0.0, 1.0]]
    )
    combined, used_replay = combine_fresh_and_replay(
        fresh, buffer, delta=0.5, batch_size=4
    )
    print(f"Fresh: 2 questions, Combined: {len(combined)//2} questions")
    print(f"Used replay: {used_replay}")

